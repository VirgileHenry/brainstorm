#[derive(idris::Idris)]
#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash)]
pub enum EnglishKeyword {
    Already,
    Additional,
    Among,
    And,
    AndOr,
    Another,
    Are,
    As,
    At,
    Be,
    Beginning,
    By,
    Cant,
    Copy,
    Divided,
    During,
    Do,
    Equal,
    Except,
    For,
    From,
    Has,
    Have,
    Into,
    If,
    In,
    Instead,
    Is,
    It,
    Kind,
    Less,
    May,
    More,
    No,
    Of,
    On,
    Only,
    Onto,
    Or,
    RatherThan,
    Than,
    That,
    The,
    Them,
    Then,
    There,
    To,
    Top,
    Unless,
    Until,
    Was,
    When,
    Whenever,
    Where,
    With,
    Without,
    Would,
}

impl EnglishKeyword {
    pub fn try_from_str(source: &str) -> Option<Self> {
        match source {
            "already" => Some(EnglishKeyword::Already),
            "additional" => Some(EnglishKeyword::Additional),
            "among" => Some(EnglishKeyword::Among),
            "and" => Some(EnglishKeyword::And),
            "and/or" => Some(EnglishKeyword::AndOr),
            "another" => Some(EnglishKeyword::Another),
            "as" => Some(EnglishKeyword::As),
            "at" => Some(EnglishKeyword::At),
            "are" => Some(EnglishKeyword::Are),
            "be" => Some(EnglishKeyword::Be),
            "beginning" => Some(EnglishKeyword::Beginning),
            "by" => Some(EnglishKeyword::By),
            "can't" => Some(EnglishKeyword::Cant),
            "copy" => Some(EnglishKeyword::Copy),
            "divided" => Some(EnglishKeyword::Divided),
            "during" => Some(EnglishKeyword::During),
            "do" => Some(EnglishKeyword::Do),
            "equal" => Some(EnglishKeyword::Equal),
            "except" => Some(EnglishKeyword::Except),
            "from" => Some(EnglishKeyword::From),
            "for" => Some(EnglishKeyword::For),
            "has" => Some(EnglishKeyword::Has),
            "have" | "'ve" => Some(EnglishKeyword::Have),
            "into" => Some(EnglishKeyword::Into),
            "if" => Some(EnglishKeyword::If),
            "in" => Some(EnglishKeyword::In),
            "instead" => Some(EnglishKeyword::Instead),
            "is" | "'s" => Some(EnglishKeyword::Is),
            "it" => Some(EnglishKeyword::It),
            "kind" => Some(EnglishKeyword::Kind),
            "less" => Some(EnglishKeyword::Less),
            "may" => Some(EnglishKeyword::May),
            "more" => Some(EnglishKeyword::More),
            "no" => Some(EnglishKeyword::No),
            "of" => Some(EnglishKeyword::Of),
            "on" => Some(EnglishKeyword::On),
            "only" => Some(EnglishKeyword::Only),
            "onto" => Some(EnglishKeyword::Onto),
            "or" => Some(EnglishKeyword::Or),
            "rather than" => Some(EnglishKeyword::RatherThan),
            "than" => Some(EnglishKeyword::Than),
            "that" => Some(EnglishKeyword::That),
            "the" => Some(EnglishKeyword::The),
            "them" => Some(EnglishKeyword::Them),
            "then" => Some(EnglishKeyword::Then),
            "there" => Some(EnglishKeyword::There),
            "to" => Some(EnglishKeyword::To),
            "top" => Some(EnglishKeyword::Top),
            "unless" => Some(EnglishKeyword::Unless),
            "until" => Some(EnglishKeyword::Until),
            "was" => Some(EnglishKeyword::Was),
            "when" => Some(EnglishKeyword::When),
            "whenever" => Some(EnglishKeyword::Whenever),
            "where" => Some(EnglishKeyword::Where),
            "with" => Some(EnglishKeyword::With),
            "without" => Some(EnglishKeyword::Without),
            "would" => Some(EnglishKeyword::Would),
            _ => None,
        }
    }
}
