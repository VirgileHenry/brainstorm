#[derive(idris::Idris)]
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, PartialOrd, Ord)]
pub enum PlayerAction {
    Add,
    Attach,
    Attack,
    Cast,
    Change,
    Choose,
    Create,
    Destroy,
    Discard,
    Double,
    Draw,
    Exile,
    Gain,
    LookAt,
    Lose,
    Pay,
    Play,
    Put,
    Return,
    Sacrifice,
    Scry,
    Search,
    Shuffle,
    Spend,
    Tap,
    Untap,
}

impl PlayerAction {
    pub fn try_from_str(source: &str) -> Option<Self> {
        match source {
            "add" | "adds" => Some(PlayerAction::Add),
            "attach" | "attaches" => Some(PlayerAction::Attach),
            "attack" | "attacks" => Some(PlayerAction::Attack),
            "cast" | "casts" => Some(PlayerAction::Cast),
            "change" | "changes" => Some(PlayerAction::Change),
            "choose" | "chooses" | "choice" => Some(PlayerAction::Choose),
            "create" | "creates" => Some(PlayerAction::Create),
            "destroy" | "destroys" => Some(PlayerAction::Destroy),
            "discard" | "discards" => Some(PlayerAction::Discard),
            "double" | "doubles" => Some(PlayerAction::Double),
            "draw" | "draws" => Some(PlayerAction::Draw),
            "exile" | "exiles" => Some(PlayerAction::Exile),
            "gain" | "gains" | "gained" => Some(PlayerAction::Gain),
            "look at" | "looks at" => Some(PlayerAction::LookAt),
            "lose" | "loses" => Some(PlayerAction::LookAt),
            "pay" | "pays" => Some(PlayerAction::Pay),
            "play" | "plays" => Some(PlayerAction::Play),
            "put" | "puts" => Some(PlayerAction::Play),
            "return" | "returns" => Some(PlayerAction::Return),
            "sacrifice" | "sacrifices" => Some(PlayerAction::Sacrifice),
            "scry" | "scrys" => Some(PlayerAction::Scry),
            "search" | "searchs" => Some(PlayerAction::Search),
            "shuffle" | "shuffles" => Some(PlayerAction::Shuffle),
            "spend" | "spends" => Some(PlayerAction::Spend),
            "tap" | "taps" => Some(PlayerAction::Tap),
            "untap" | "untaps" => Some(PlayerAction::Untap),
            _ => None,
        }
    }
}
